#ifndef MATH_UTILS_HPP
#define MATH_UTILS_HPP

/**
 * @brief returns the sine of x in degrees
 */
float sin_deg(float x);

/**
 * @brief returns the cosine of x in degrees
 */
float cos_deg(float x);

/**
 * @brief returns the tangent of x in degrees
 */
float tan_deg(float x);

/**
 * @brief returns the modulus of a and b
 * @param a the dividend
 * @param b the divisor
 * @return the modulus of a and b
 */
int mod(int a, int b);

#endif // MATH_UTILS_HPP
